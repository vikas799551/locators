package firstproject;

public class Demoassignment {

	public int Simpleassign(int a,int b) {
		return a=b;
	}
	public int Add_Equal(int a,int b) {
		return a+=b;
	}
	public int Sub_Equal (int a,int b) {
		return a-+b;
	}
	public int Multi_Equal(int a,int b) {
		return a*=b;
	}
	public int Div_Equal(int a,int b) {
		return a/=b;
	}
	public int Modulu_Equal(int a,int b) {
		return a%=b; 
	}
	
	
	
}

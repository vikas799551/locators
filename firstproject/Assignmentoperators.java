package firstproject;

public class Assignmentoperators {

	public static void main(String[] args) {
		
		int x = 30;
		int y = 10;
		
		x += y;
		System.out.println("x +=y: +x");
		
		x -=y;
		System.out.println("x -=y: +x");
		
		x *=y;
		System.out.println("x *=y: +x");
		
		x /=y;
		System.out.println("x /=y: +x");
		
		x %=y;
		System.out.println("x %=y: +x");
		
	}

}

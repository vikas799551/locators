package amazon;

public class MethodOverLoading {

	
	///Method Overloading 
	
	
	public int Add(int number_One ,int number_Two){
		return number_One + number_Two;
	}
	
	
	public String Add(String number_One ,String number_Two){
		return number_One + number_Two;
	}
	
	public double  Add(double number_One ,double number_Two){
		return number_One + number_Two;
	}
	
	public float Add(float number_One ,float number_Two){
		return number_One + number_Two;
	}
	
	
	
	
	
}
